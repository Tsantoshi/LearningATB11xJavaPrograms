package ex_02_Literals;

public class Literals_P1 {
    public static void main(String[] args) {

        //literal

        int age=65;
        System.out.println(age);
        age=19;
        System.out.println(age);

        //constant

        final int girl_age=18;  // final is a keyword
       // girl_age=19;

        float pi = 3.14f;
        float pi2 = 3.14F;
        float x = 10.00f;

        System.out.println(pi);

        int Enum = 9; // enum is keyword
        System.out.println(Enum);




    }
}
