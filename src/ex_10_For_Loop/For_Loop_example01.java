package ex_10_For_Loop;

public class For_Loop_example01 {
    public static void main(String[] args) {
        // For Loop
        // Help you to repeat a block of code.

        // Initialization -> Condition -> Updation(Incre/decre)
        // ICU
        int i =10;

        for( i=0; i<=10; i++){
            System.out.println(i);
        }
    }
}
